#include "minishell.h"

int	is_line_empty(char	*str)
{
	size_t	i;

	i = 0;
	while (str[i] !='\0')
	{
		if (str[i] != 32 && (str[i] <= 9 || str[i] >= 13))
			return (1);
		i++;
	}
	return (0);
}
